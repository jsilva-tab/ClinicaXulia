    private void cadastrarCliente(){
        Cliente newCliente = new Cliente();
        
        newCliente.setCpf(txtCPF.getText());
        newCliente.setNome(txtNome.getText());
        newCliente.setEmail(txtEmail.getText());
        newCliente.setSenha(new String(txtSenha.getPassword()));
        newCliente.setTelefone(txtTelefone.getText());
        newCliente.setEndereco(txtEndereco.getText());
        
        try {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate dataNasc = LocalDate.parse(txtDataNascimento.getText(), formatter);
        newCliente.setData_nasc(dataNasc);
        } catch (DateTimeParseException e) {
        JOptionPane.showMessageDialog(this, "Data de nascimento inválida.");
        }
        
        ClienteDAO clienteDAO = new ClienteDAO();
        
        try {
            clienteDAO.cadastrarCliente(newCliente);
            JOptionPane.showMessageDialog(null,"Cliente cadastrado!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"erro criar cliemte jif:"+e);
        }
    }
    
    private void excluirCliente(){
        String cpfCliente = txtCPF.getText();
        ClienteDAO clienteDAO = new ClienteDAO();
        
        int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir este cliente?", "Excluir Cliente", JOptionPane.YES_NO_OPTION);
        if (confirmacao == JOptionPane.YES_OPTION) {
            clienteDAO.excluirCliente(cpfCliente);
            JOptionPane.showMessageDialog(null, "Cliente excluído com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao excluir cliente.");
        }
    }
