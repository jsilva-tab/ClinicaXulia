    public void listarClientes(){
        try {
            ClienteDAO cliDAO = new ClienteDAO();
            DefaultTableModel model = (DefaultTableModel) tabelaVisuClientes.getModel();
            model.setNumRows(0);
            
            List<Cliente> lista = cliDAO.listarClientes();
            
            for(int num=0; num<lista.size(); num++){
                model.addRow(new Object[]{
                    lista.get(num).getCpf(),
                    lista.get(num).getNome(),
                    lista.get(num).getEmail(),
                    lista.get(num).getSenha(),
                    lista.get(num).getTelefone(),
                    lista.get(num).getEndereco(),
                    lista.get(num).getData_nasc()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"erro listar cli jif:"+e);
        }
    }
    
    private void buscarCliente() {
        String sql = "select * from tb_clientes where nome_cliente like ?";
        
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtCampoBusca.getText()+"%");
            rs = pst.executeQuery();
            tabelaVisuClientes.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    private void updateCliente(){
    Cliente cliente = new Cliente();
    cliente.setNome(txtNome.getText());
    cliente.setEmail(txtEmail.getText());
    cliente.setSenha(new String(txtSenha.getPassword())); 
    cliente.setTelefone(txtTelefone.getText());
    cliente.setEndereco(txtEndereco.getText());

    // Converte a data de nascimento para LocalDate
    try {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate dataNasc = LocalDate.parse(txtDataNascimento.getText(), formatter);
        cliente.setData_nasc(dataNasc);
    } catch (DateTimeParseException e) {
        JOptionPane.showMessageDialog(this, "Data de nascimento inválida.");
        return; 
    }

    cliente.setCpf(txtCPF.getText()); 

    ClienteDAO clienteDAO = new ClienteDAO();

    try {
        boolean sucesso = clienteDAO.atualizarCliente(cliente);
        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Dados do cliente atualizados com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar os dados.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao salvar os dados: " + e.getMessage());
    }    
   }
    
